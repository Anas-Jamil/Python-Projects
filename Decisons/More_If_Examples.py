# read a letter from user. If it is 'A' or 'a' print a message.
# For any other letter print a different message
Letter = input("Enter a letter: ")
if Letter == "A":
    print("Thats a capital A.")
elif Letter == "a":
    print("That's a lowercase a.")
else:
    print("no")